io = require"io"
getModuleProcesAddress = function(modulo)
  for linha in io:lines"/proc/self/maps" do
    local a = linha:match"^(%x+)-"
    if a and linha:find(modulo,1, true) then 
      return tonumber(a, 16)
    end
  end
end



